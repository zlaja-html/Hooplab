const menuButton = document.querySelector('.menu-toggle');
const nav = document.querySelector('#site-nav');
const langButtons = document.querySelectorAll('[data-language]');
const form = document.querySelector('#apply-form');
const metaDescription = document.querySelector('meta[name="description"]');
const storedLanguage = window.localStorage.getItem('hooplab-language');
let currentLanguage = storedLanguage && translations[storedLanguage] ? storedLanguage : 'en';

function t(key) {
  const value = key.split('.').reduce((result, part) => result?.[part], translations[currentLanguage]);
  return typeof value === 'string' ? value : key;
}

function applyLanguage(language) {
  if (!translations[language]) {
    return;
  }

  currentLanguage = language;
  document.documentElement.lang = language;
  document.title = translations[language].metaTitle;

  if (metaDescription) {
    metaDescription.setAttribute('content', translations[language].metaDescription);
  }

  document.querySelectorAll('[data-i18n]').forEach(element => {
    element.textContent = t(element.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-html]').forEach(element => {
    element.innerHTML = t(element.dataset.i18nHtml);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    element.setAttribute('placeholder', t(element.dataset.i18nPlaceholder));
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach(element => {
    element.setAttribute('aria-label', t(element.dataset.i18nAriaLabel));
  });

  langButtons.forEach(button => {
    const isActive = button.dataset.language === language;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });

  if (form) {
    const submitButton = form.querySelector('button[type="submit"]');
    if (submitButton && !submitButton.disabled) {
      submitButton.textContent = t('form.submit');
    }
  }

  if (form) {
    syncScheduler?.();
  }

  window.localStorage.setItem('hooplab-language', language);
}

